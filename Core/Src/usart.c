//串口数据帧校验
int Data_Test(uint8_t end)
{
	if(rx_buf_imu[0] != 0x55) return 0;
	if(rx_buf_imu[1] != 0x51 && rx_buf_imu[1] != 0x52 && rx_buf_imu[1] != 0x53) return 0;
	if(rx_buf_imu[10] != (uint8_t)(0x55+end+rx_buf_imu[2]+rx_buf_imu[3]+rx_buf_imu[4]+rx_buf_imu[5]+rx_buf_imu[6]+rx_buf_imu[7]+rx_buf_imu[8]+rx_buf_imu[9])) return 0;
	return 1;
}


//串口回调函数
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	//串口屏的串口回调函数，用于小车模式的转换
	if(huart == &huart2)
	{

		rx_buf_cam[rx_count_cam++] = Received_Data_Cam;
		if(rx_buf_cam[0] != 0x55) rx_count_cam = 0;
		if(rx_count_cam == 3)
		{
			if(rx_buf_cam[2] == 0x37)
			{
				mode = rx_buf_cam[1];
			}				
			rx_count_cam = 0;
		}
		HAL_UART_Receive_IT(&huart2, &Received_Data_Cam, 1);
	}
	//IMU的串口回调函数
	if(huart == &huart1)
	{
		rx_buf_imu[rx_count_imu++] = Received_Data_IMU;

		if(rx_buf_imu[0] != 0x55) rx_count_imu = 0;

		if(rx_count_imu == 2 && rx_buf_imu[1] != 0x51 && rx_buf_imu[1] != 0x52 && rx_buf_imu[1] != 0x53) rx_count_imu =0;

		if(rx_buf_imu[1] == 0x51)
		{
			if(rx_count_imu == 11)
			{
				if(Data_Test(0x51))
				{
					uint8_t acc_x_L = rx_buf_imu[2];
					uint8_t acc_x_H = rx_buf_imu[3];
					uint8_t acc_y_L = rx_buf_imu[4];
					uint8_t acc_y_H = rx_buf_imu[5];
					uint8_t acc_z_L = rx_buf_imu[6];
					uint8_t acc_z_H = rx_buf_imu[7];
					
					acc_x = (int16_t)((acc_x_H<<8)|acc_x_L);
					acc_y = (int16_t)((acc_y_H<<8)|acc_y_L);
					acc_z = (int16_t)((acc_z_H<<8)|acc_z_L);
				}
				rx_count_imu = 0;
			}
		}
		else if(rx_buf_imu[1] == 0x52)
		{
			if(rx_count_imu == 11)
			{
				if(Data_Test(0x52))
				{
					uint8_t angle_vx_L = rx_buf_imu[2];
					uint8_t angle_vx_H = rx_buf_imu[3];
					uint8_t angle_vy_L = rx_buf_imu[4];
					uint8_t angle_vy_H = rx_buf_imu[5];
					uint8_t angle_vz_L = rx_buf_imu[6];
					uint8_t angle_vz_H = rx_buf_imu[7];
					
					angle_vx = (int16_t)((angle_vx_H<<8)|angle_vx_L);
					angle_vy = (int16_t)((angle_vy_H<<8)|angle_vy_L);
					angle_vz = (int16_t)((angle_vz_H<<8)|angle_vz_L);
				}
				rx_count_imu = 0;
			}
		}
		else if(rx_buf_imu[1] == 0x53)
		{
			if(rx_count_imu == 11)
			{
				if(Data_Test(0x53))
				{
					uint8_t Role_L = rx_buf_imu[2];
					uint8_t Role_H = rx_buf_imu[3];
					uint8_t Pitch_L = rx_buf_imu[4];
					uint8_t Pitch_H = rx_buf_imu[5];
					uint8_t Yaw_L = rx_buf_imu[6];
					uint8_t Yaw_H = rx_buf_imu[7];
					
					Role = (int16_t)((Role_H<<8)|Role_L);
					Pitch = (int16_t)((Pitch_H<<8)|Pitch_L);
					Yaw = (int16_t)((Yaw_H<<8)|Yaw_L);
				}
				rx_count_imu = 0;
			}
		}
		HAL_UART_Receive_IT(&huart1, &Received_Data_IMU, 1);
	}
	
}

int IMU_Init_Yaw(void)
{
	int i = 0;
	int Yaw_average = 0;
	for(i = 0; i < 100; i++)
	{
		Yaw_average += Yaw;
	}
	return Yaw_average/100;
}
